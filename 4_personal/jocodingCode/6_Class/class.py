# 클래스란 반복되는 변수와 메서드(함수)를 미리 정해놓은 틀(설계도)이다.
# 모듈이란 미리 만들어놓은 .py(파이썬) 파일이다. (함수, 변수, 클래스)
# 패키지란 모듈을 여러 개 모아놓은 것이다. (패키지=라이브러리)

# 오버라이딩이란 클래스 상속 시 부모 클래스에서 정의한 메소드를 자식 클래스에서 변경하는 것이다.
# 부모 클래스의 메소드 이름과 기본적인 기능은 그대로 사용하지만, 특정 기능을 바꾸고 싶을 때 사용한다.

# 오버로딩이란 동일한 클래스 내에서 매개변수의 자료형 또는 개수가 다른 동명의 메소드를 정의하는 것이다.
# 즉, 클래스 내에서 같은 이름의 메소드를 여러 개 선언하는 것이다.
# 다양한 매개변수를 받아 같은 이름의 여러 개의 함수를 한번에 처리하기 위함이다.
# 파이썬에서는 오버로딩을 정식적으로 지원하지 않는다.
